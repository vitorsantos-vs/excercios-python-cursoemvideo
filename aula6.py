print("Algo é isdecimal", algo.isdecimal())
print("Algo é isdigit", algo.isdigit())
print("Algo é isidentifier", algo.isidentifier())
print("Algo é islower", algo.islower())
print("Algo é isnumeric", algo.isnumeric())
print("Algo é isprintable", algo.isprintable())
print("Algo é isspace", algo.isspace())
print("Algo é istatle", algo.istitle())
