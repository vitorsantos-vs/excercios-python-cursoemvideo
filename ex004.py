
algo = input("Digite algo: ")
print("O tipo primitivo desse valor é: ", type(algo))
print("É alfabetico? {}".format(algo.isalpha()))
print("É um número? {}".format(algo.isnumeric()))
print("É alfanumerico? {}".format(algo.isalnum()))
print("So tem espaços? {}".format(algo.isspace()))
print("Esta em Maiscula? {}".format(algo.isupper()))
print("Esta em minuscula? {}".format(algo.islower()))
print("É capitalizado/titulo? {}".format(algo.istitle()))
print("É decimal? {}".format(algo.isdecimal()))



